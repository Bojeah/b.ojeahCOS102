tuple = ("Ekiti", 750, 'Oshogbo', 250, "Akure", 500)
s_tuple = ("Abeokuta", 300, "Ogomboso")

#prints the complete tuple
print(tuple)

#prints last element of the tuple
print(tuple[-1])

#prints the third and 4th element of the tuple
print(tuple[2:4])

#prints the fourth till the last element of the tuple
print(tuple[3:])

#prints the tuple three times
print(s_tuple * 3)

#prints concatenated tuples
print(tuple + s_tuple)